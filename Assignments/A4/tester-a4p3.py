def isTree(G, T):
    # Basic tests first against G
    if len(T) != len(G):
        return False
    for u in range(len(T)):
        for v in T[u]:
            if v not in G[u]:
                return False

    # Now BFS on T to confirm that it's a tree
    # But need to adapt BFS somewhat carefully
    colour = list()
    pi = list()
    for i in range(len(T)):
        colour.append(False)  # False == WHITE
        pi.append(-1)  # -1 == NIL
    Q = list()
    Q.append(0)
    colour[0] = True
    while Q:
        u = Q.pop(0)
        for v in T[u]:
            if not colour[v]:
                colour[v] = True
                pi[v] = u
                Q.append(v)
            elif pi[u] != v:  # This is the key check
                return False
    return True


def isDifferent(P, Q):
    # Are the graphs P and Q different?
    if (len(P) != len(Q)):
        return True

    for u in range(len(P)):
        for v in P[u]:
            if v not in Q[u]:
                return True

    for u in range(len(Q)):
        for v in Q[u]:
            if v not in P[u]:
                return True

    return False


def graphCopy(G):
    H = list()
    for i in range(len(G)):
        H.append(list())
        for j in G[i]:
            (H[i]).append(j)
    return H


